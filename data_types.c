#include<stdio.h>
int main(){
    int a = 10;
    float b = 10.25f;
    short s = 32767;
    long l = 2147483647;
    double d = 3.14159265358979323846;
    long double lb = 3.141592653589793238462643383279502884197169399375105820974944592;
    char c = 'c';
    char name[9] = "Prasaanth";
    printf("This Data Type of a is Interger : %d\n",a);
    printf("This Data Type of b is Float : %f\n",b);
    printf("This Data Type of s is Short :%d\n",s);
    printf("This Data Type of l is Long : %ld\n",l);
    printf("This Data Type of d is Double : %lf\n",d);
    printf("This Data Type of lb is Long Double : %Lf\n",lb);
    printf("This Data Type of c is Char : %c",c);
    printf("This Data Type of name is String : %s\n",name);

}